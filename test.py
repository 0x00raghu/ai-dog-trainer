# Python code to print a simple dot art

# Define the art pattern using a list of strings
dot_art = [
    "....*....",
    "...***...",
    "..*****..",
    ".*******.",
    "*********",
    ".*******.",
    "..*****..",
    "...***...",
    "....*...."
]

# Print each line of the dot art
for line in dot_art:
    print(line)